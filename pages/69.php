  I came out of my waking dream with temples<br>
  moist and tongue most damnably dry. I had to<br>
  believe myself, for I had never previously<br>
  deceived myself. Yes, I came to myself, if you<br>
  must know, when yon same star, that’s west-<br>
  ward from the pole, had made his course to<br>
  illume that part of heaven where now it burns.<br>
  A time, I thought, not only utterly depressing<br>
  in itself, but also, when one is alone, as Dutch<br>
  as dillwater. What, I asked myself, ought I to<br>
  do? The answer was plain enough. When I<br>
  was ten I had messily collected eggs. When I<br>
  was twelve I had collected bus tickets and,<br>
  if I had known where to look for them,<br>
  would doubtless have collected whole ickets<br>
  also. The answer was plain enough. I<br>
  must—oh, final and most difficult hobby!—
  collect myself. It was ghastly. I had seen<br>
  every minute of it. I had seen a poor old<br>
  man done slowly to death before my eyes.<br>

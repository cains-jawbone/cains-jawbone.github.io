  As I was not staying, but only passing through,<br>
  I raised my hat to the eleven that played All<br>
  England for a thousand guineas, and beat<br>
  them twenty-nine times in ten years. I paid<br>
  respect also to a couple of exceptionally large<br>
  yews. After all, I was doing another man’s<br>
  work for him. As I progressed, I began to<br>
  remember what my favourite author had<br>
  called him. He had called him lovely and<br>
  soothing, and delicate. He had called him<br>
  cool-enfolding and a dark mother. From me<br>
  to thee, he had said, glad serenades, dances<br>
  for thee I propose saluting thee. Also vast and<br>
  well-veil’d. But somehow I had my doubts. I<br>
  sat on the grass, and counted a distinct<br>
  ninety between each beat of my heart. I<br>
  would have to go slow. Each beat, I saw,<br>
  puffed out of my breast like purple smoke<br>
  from an exhaust. I dance with the dancers<br>
  and drink with the drinkers. The echoes<br>
  ring with our indecent calls, I pick out<br>
  some low person for my dearest friend.<br>
